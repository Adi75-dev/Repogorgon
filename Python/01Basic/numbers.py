import math  # Math-module
x = 10
x = 0b10
print(x)
print(bin(x))  # Instead of line-2, write as given
x = 0x12c
print(x)  # for hexadecimal
print(hex(x))

# Complex Numbers - a+bi
x = 1+2j  # J or j is an imaginary number
print(x)
x = 10 + 3
x = 10 - 3
x = 10 * 3
x = 10 / 3
print(x)
x = 10 // 3  # for integer value
print(x)
x = 10 % 3
x = 10 ** 3  # for Exponent
print(x)
x = x + 1
x += 1  # Augmented Operator
print(x)

# Functions with numbers
PI = 3.14  # All caps to indicate it as a constant to other devs
print(round(PI))
print(abs(PI))
print(math.floor(PI))

